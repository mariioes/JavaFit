    private void botonDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDatosActionPerformed
// 1. Verificamos que el socio no sea null
        if (this.socioActual != null) {
            VentanaDatosUsuario vdu = new VentanaDatosUsuario(this, this.socioActual);
            vdu.setVisible(true);
            this.setVisible(false);
            
        } else {
            // Si sale este error, el problema viene del Login
            System.out.println("ERROR: socioActual es null en el Menú");
            javax.swing.JOptionPane.showMessageDialog(this, "Error crítico: No se encontraron los datos del usuario.");
        }
    }//GEN-LAST:event_botonDatosActionPerformed

